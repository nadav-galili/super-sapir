export interface CategoryYtdStats {
  category: string;
  ytdSales: number;
  ytdSalesLastYear: number;
  ytdGrowthPct: number;
  chainYtdGrowthPct: number;
  vsChainPct: number;
  monthlySales: number[];
  monthlyTargets: number[];
  monthlyLastYear: number[];
}

const CHAIN_YTD_GROWTH_PCT = 6.4;

function buildYtdStats(
  category: string,
  ytdSales: number,
  ytdSalesLastYear: number,
  seasonality: number[]
): CategoryYtdStats {
  const ytdGrowthPct = +(
    ((ytdSales - ytdSalesLastYear) / ytdSalesLastYear) *
    100
  ).toFixed(1);
  const seasonalitySum = seasonality.reduce((a, b) => a + b, 0);
  const monthlySales = seasonality.map((w) =>
    Math.round((ytdSales * w) / seasonalitySum)
  );
  const monthlyTargets = monthlySales.map((v) => Math.round(v * 1.04));
  const monthlyLastYear = seasonality.map((w) =>
    Math.round((ytdSalesLastYear * w) / seasonalitySum)
  );
  return {
    category,
    ytdSales,
    ytdSalesLastYear,
    ytdGrowthPct,
    chainYtdGrowthPct: CHAIN_YTD_GROWTH_PCT,
    vsChainPct: +(ytdGrowthPct - CHAIN_YTD_GROWTH_PCT).toFixed(1),
    monthlySales,
    monthlyTargets,
    monthlyLastYear,
  };
}

